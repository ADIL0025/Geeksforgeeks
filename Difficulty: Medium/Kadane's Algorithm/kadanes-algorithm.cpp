class Solution {
  public:
    // Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(vector<int> &arr) {
        // code here...
        long long curr=0;
       long long maxTill=INT_MIN;
       
       for(long long i=0; i<arr.size(); i++){
           curr += arr[i];
           maxTill = max(curr, maxTill);
           
           if(curr<0){
               curr=0;
           }
       }
       return maxTill;
    }
};

